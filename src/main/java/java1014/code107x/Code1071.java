package java1014.code107x;

import java.util.Scanner;

public class Code1071 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        while (true) {
            int i = sc.nextInt();
            if (i == 0) {
                break;
            }
            System.out.println(i);
        }
    }
}
