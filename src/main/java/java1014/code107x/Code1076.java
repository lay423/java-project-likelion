package java1014.code107x;

import java.util.Scanner;

public class Code1076 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        char c = sc.next().charAt(0);
        char c1 = 'a';
        for (c1 = 'a'; c1 <= c; c1++) {
            System.out.print(c1+" ");
        }
    }
}
