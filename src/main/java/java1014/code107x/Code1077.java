package java1014.code107x;

import java.util.Scanner;

public class Code1077 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int sn = 0;
        for (sn = 0; sn <= n; sn++) {
            System.out.println(sn);
        }
    }
}
