package java1014.code107x;

import java.util.Scanner;

public class Code1079 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        while (true) {
            char i = sc.next().charAt(0);
            System.out.println(i);
            if (i == 'q') {
                break;
            }

        }
    }
}
